def main():
    my_string1 = 'InterviewBit'
    my_string2 = 'Don\'t change the variable name'
    my_int = 11
    my_float = 20.0
    
    print("String1: %s" % my_string1)
    print("String2: %s" % my_string2)
    print("Integer: %d" % my_int)
    print("Float: %f" % my_float)
    return 0

if __name__ == '__main__':
    main()
